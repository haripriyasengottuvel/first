package teluskoteach;

public class Variabledemo 
{
	public static void main(String args[]){
	int a=50000_0000;//int 4 bytes =32bits=-2,147,483,648 to2,147,483,648 
	a=8;
	System.out.print(a);
	double b=5.5;//the decimal values are considered as doublr by default
	float c=5.5f;//terminate the number with f
	short d=9;//short is same as int having 2 byte= -32768 to 32767
	byte e=22;//1 byte=8bits=-128 to 127
	long f=50000000000l;
	char g='A';
	g=69;//character takes a value of ascii code-American standard code for information interchange
	double b1=5.5;//implicit conversion
	int a1=(int)5.7;//type casting
	System.out.print(b1);
	}
	

}
